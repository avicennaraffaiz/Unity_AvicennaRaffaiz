public class Dosen {
    private String name;
    private int id;
    
    public Dosen(String name, int id) {
        this.name = name;
        this.id = id;
    }
    
    public String getname() {
        return name;
    }
    
    public void setName(String name) {
        this.name = name;
    }
    
    public int getId() {
        return id;
    }
    
    public void setId(int id) {
        this.id = id;
    }
}
